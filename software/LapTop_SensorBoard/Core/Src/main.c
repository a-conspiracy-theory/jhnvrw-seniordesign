float Read_Internal_Temperature(void)
{
    uint16_t adc_raw_val = 0;
    float temperature_c = 0.0f;
    float vdd_voltage = 3.3f; // Target operating voltage


    // Configure the ADC channel dynamically
    if (HAL_ADC_ConfigChannel(&hadc1, &ConfigChannel) == HAL_OK)
    {
        HAL_ADC_Start(&hadc1);

        // Wait for conversion with a 2ms timeout
        if (HAL_ADC_PollForConversion(&hadc1, 2) == HAL_OK)
        {
            adc_raw_val = HAL_ADC_GetValue(&hadc1);

            // Calculate voltage from raw 12-bit ADC data
            float vsense = (float)(adc_raw_val * vdd_voltage) / 4095.0f;

            // Standard STM32 conversion calculation (V25 offset and Slope)
            temperature_c = ((vsense - 1.3f) / 0.0043f) + 15.0f;
        }

        HAL_ADC_Stop(&hadc1);
    }

    return temperature_c;
}

